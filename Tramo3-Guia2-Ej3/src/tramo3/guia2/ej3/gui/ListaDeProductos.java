    private void jbAgregarProductoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jbAgregarProductoActionPerformed
        try{
        String categoria = jtfCategoria.getText();
        String producto = jtfProducto.getText();
        double precio = Double.parseDouble(jtfPrecio.getText());
        
        Datos d = new Datos (producto, precio);
        Categoria c = new Categoria (categoria);
        cargarDatos(d, c);
        
        } catch(NumberFormatException e){
            
            if (jtfCategoria.getText().isEmpty() || jtfProducto.getText().isEmpty()) {
            JOptionPane.showMessageDialog(this, "No debe haber campos vacios");
           return ;
        }else if (jtfPrecio.getText().isEmpty()) {
                
            JOptionPane.showMessageDialog(this, "Debe cargar un precio");
            
            }else{
            
            JOptionPane.showMessageDialog(this, "Precio mal ingresado");
            }
            
        }finally{
            jtfProducto.setText("");
            jtfPrecio.setText("");
        }
        
    }//GEN-LAST:event_jbAgregarProductoActionPerformed

