    private void cargarCombo(){
        jcbProductos.addItem(new Categoria("Electronica"));
        jcbProductos.addItem(new Categoria("Ropa"));
        jcbProductos.addItem(new Categoria("Alimento"));
    }
    
    private void armarCabecera(){
        modelo.addColumn("Producto");
        modelo.addColumn("Categoria");
        modelo.addColumn("Precio");
        jtableProductos.setModel(modelo);
    }
    
    private void cargarDatos(Datos e, Categoria i){
        modelo.addRow(new Object []{e.getProducto(),i.getCategoria(),e.getPrecio()});
    }


